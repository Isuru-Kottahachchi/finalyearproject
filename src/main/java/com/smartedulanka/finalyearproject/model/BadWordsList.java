package com.smartedulanka.finalyearproject.model;

public class BadWordsList {

    public int deviations;
    public int end;
    public int info;
    public String original;
    public int replacedLen;
    public int start;
    public String word;

    public int getDeviations() {
        return deviations;
    }

    public void setDeviations(int deviations) {
        this.deviations = deviations;
    }

    public int getEnd() {
        return end;
    }

    public void setEnd(int end) {
        this.end = end;
    }

    public int getInfo() {
        return info;
    }

    public void setInfo(int info) {
        this.info = info;
    }

    public String getOriginal() {
        return original;
    }

    public void setOriginal(String original) {
        this.original = original;
    }

    public int getReplacedLen() {
        return replacedLen;
    }

    public void setReplacedLen(int replacedLen) {
        this.replacedLen = replacedLen;
    }

    public int getStart() {
        return start;
    }

    public void setStart(int start) {
        this.start = start;
    }

    public String getWord() {
        return word;
    }

    public void setWord(String word) {
        this.word = word;
    }
}
